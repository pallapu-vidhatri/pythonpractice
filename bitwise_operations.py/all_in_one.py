a = 2
b = 3
result_and = a & b
result_or = a | b
result_xor = a ^ b
result_not_a = ~a
result_left_shift = a << 1
result_right_shift = a >> 1
print("AND:", result_and)               # Output: 2
print("OR:", result_or)                 # Output: 3
print("XOR:", result_xor)               # Output: 1
print("NOT a:", result_not_a)           # Output: -3
print("Left Shift a by 1:", result_left_shift)   # Output: 4
print("Right Shift a by 1:", result_right_shift) # Output: 1
