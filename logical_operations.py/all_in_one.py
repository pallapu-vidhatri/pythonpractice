a = int(input("Enter first integer: "))
b = int(input("Enter second integer: "))

result = a and b
print("The result of logical AND operation is:", result)

result = a or b
print("The result of logical OR operation is:", result)

result = not a
print("The result of logical NOT operation on first integer is:", result)

result = not b
print("The result of logical NOT operation on second integer is:", result)

